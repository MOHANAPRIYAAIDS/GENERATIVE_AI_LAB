def clean_job_skills(job_df):
    job_df["Skill"] = job_df["Skill"].apply(lambda x: ast.literal_eval(x) if isinstance(x, str) else [])
    job_df["Skill"] = job_df["Skill"].apply(lambda x: [s.lower() for s in x])
    return job_df

job_df = clean_job_skills(job_df)

def build_skill_vocab(job_df):
    skills = set()
    for s in job_df["Skill"]:
        skills.update(s)
    return list(skills)

