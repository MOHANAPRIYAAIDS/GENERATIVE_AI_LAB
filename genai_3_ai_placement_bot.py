def full_pipeline(resume_text, role, user_answer):

    # Resume
    resume_result = resume_analysis(resume_text, role)

    # Skills
    user_skills = extract_skills(resume_text)
    job_skills = get_job_skills(role)

    # Gap
    gap = skill_gap_prompt(user_skills, job_skills, role)

    # Courses
    courses = recommend_courses(user_skills)

    # Learning plan
    plan = learning_plan(user_skills)

    # Interview
    question, ideal = generate_question(role)

    # Evaluation
    evaluation = evaluate_answer(question, user_answer, ideal)

    return {
        "resume_analysis": resume_result,
        "skills": user_skills,
        "skill_gap": gap,
        "courses": courses,
        "learning_plan": plan,
        "question": question,
        "ideal_answer": ideal,
        "evaluation": evaluation
    }

